    AREA SL32,CODE,READONLY

SMALL RN R0
LARGE RN R1
NUM RN R3

    LDR R2,=ARRAY
    LDR SMALL,[R2],#4
    MOV LARGE,SMALL

NEXT
    LDR NUM,[R2],#4
    CMP R2,ARRAYEND
    BEQ STOP
    CMP SMALL,NUM
    BLS CHK_LRG
    MOV SMALL,NUM
    B NEXT

CHK_LRG2
    CMP LARGE,NUM
    BLS NEXT
    MOV LARGE,NUM
    B NEXT

STOP   B STOP

ARRAY DCD 0x00000007, 0x00000004, 0x00000003, 0x00000009, 0x00000002, 0x00000001, 0x00000006, 0x00000005
ARRAYEND DCD 0
    
    ALIGN
    END